//这可以是一个类似于中断函数，一旦can接收到数据后就会触发这个函数
void HAL_CAN_RxFifo0MsgPendingCallback(CAN_HandleTypeDef *hcan)
{
   //判断这个can是不是can2
  if (hcan->Instance==CAN2)
  {

    CAN_RxHeaderTypeDef rx_header;  //数据的一些类型信息
    uint8_t rx_data[8];

    //判断是否通过can接收到数据
    if (HAL_CAN_GetRxMessage(&hcan2, CAN_RX_FIFO0, &rx_header, rx_data)==HAL_OK)
    {
      //调用can_motor.c中的解析函数，将数据填充到motor_feedback数组中
      CAN_Parse_Motor_Feedback(&rx_header, rx_data);
    }
  }
}


void CAN_Filter_Init(void)
{
  CAN_FilterTypeDef can_filter;

  can_filter.FilterMode = CAN_FILTERMODE_IDMASK;
  can_filter.FilterScale = CAN_FILTERSCALE_32BIT;
  can_filter.FilterIdHigh = 0x204<<5;
  can_filter.FilterIdLow = 0;
  can_filter.FilterMaskIdHigh = 0x20B<<5;
  can_filter.FilterMaskIdLow = 0;
  can_filter.FilterFIFOAssignment = CAN_RX_FIFO0;
  can_filter.FilterActivation = ENABLE;   ///激活过滤器
  can_filter.FilterBank=14;
  can_filter.SlaveStartFilterBank=14;//对于can2来说使用过滤器组14

  if (HAL_CAN_ConfigFilter(&hcan2, &can_filter)!=HAL_OK) {
    Error_Handler();
  }

}
